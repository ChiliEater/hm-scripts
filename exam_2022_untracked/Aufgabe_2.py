a = np.array([
    [3, 0, 0, ],
    [1, 3, 0, ],
    [0, 0, 6, ],
])
x0 = np.array([1, 1, 1], dtype=np.float64)

n = 35

v = x0 / np.linalg.norm(x0, ord=2)

for i in range(n):
    v = a @ v
    v = v / np.linalg.norm(v, ord=2)
    print(f'v_{i + 1} = {v}, λ_{i + 1} = {(v.T @ a @ v) / (v.T @ v)}')

print('')
print(f'v = {v}, λ = {(v.T @ a @ v) / (v.T @ v)}')

